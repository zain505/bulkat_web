function getNestedValue(source, path) {
  return path.split('.').reduce((value, key) => value?.[key], source)
}

function interpolate(template, values = {}) {
  return String(template).replace(/\{\{(\w+)\}\}/g, (_, key) => values[key] ?? '')
}

export function createI18n(language = 'en') {
  const nextLanguage = languageMeta[language] ? language : 'en'
  const meta = languageMeta[nextLanguage]

  return {
    ...meta,
    language: nextLanguage,
    t(key, values = {}) {
      const localized = getNestedValue(translations[nextLanguage], key)
      const fallback = getNestedValue(translations.en, key)
      return interpolate(localized ?? fallback ?? key, values)
    },
  }
}

export function translateStatus(t, status) {
  const rawStatus = String(status || '')
  const key = `status.${rawStatus.toLowerCase()}`
  const translated = t(key)

  return translated === key ? rawStatus : translated
}
